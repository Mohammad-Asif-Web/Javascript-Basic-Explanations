document.write("<h1>Task no.2 --  Making my own Calculator</h1>");

var num1 = parseFloat(prompt("Enter your First Number"));
var num2 = parseFloat(prompt("Enter your Second Number"));

var sum, sub, mul, div, rem;

var sum = num1 + num2;
var sub = num1 - num2;
var mul = num1 * num2;
var div = num1 / num2;
var rem = num1 % num2;



document.write(num1 + " + " + num2 + " = " + sum);
document.write("<br>");
document.write(num1 + " - " + num2 + " = " + sub);
document.write("<br>");
document.write(num1 + " * " + num2 + " = " + mul);
document.write("<br>");
document.write(num1 + " / " + num2 + " = " + div);
document.write("<br>");
document.write(num1 + " % " + num2 + " = " + rem);
document.write("<br>");















